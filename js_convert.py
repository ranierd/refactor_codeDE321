class JSConvert:

    def __init__(self):
        self.classes = []

    def build_classes(self, data: []):
        self.classes.append(data)

    def build_functions(self, data: []):
        self.classes.append(data)

    def build_attributes(self, data: []):
        self.classes.append(data)

    def merge(self, data: []):
        self.classes.append(data)

